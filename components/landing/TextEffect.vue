<script>
export default {
  mounted() {
    setTimeout(() => {
      this.textEffect()
    }, 1000)
  },
  data() {
    return {
      localTl: null,
    }
  },
  methods: {
    textEffect() {
      const gsap = this.$gsap
      //console.log(gsap)
      var tmax_optionsGlobal = {
        repeat: -1,
        repeatDelay: 0,
        yoyo: true,
      }

      this.$CSSRulePlugin.useSVGTransformAttr = true

      this.localTl = gsap.timeline(tmax_optionsGlobal)
      // path = 'svg *',

      // console.log(text)
      let paths = this.$el.querySelectorAll('svg *')
      // let parts = this.$el.querySelectorAll('polygon')

      paths.forEach((path) => {
        // console.log(path)
        this.localTl.set(path, {
          x: '+=' + getRandom(-500, 500),
          y: '+=' + getRandom(-500, 500),
          rotation: '+=' + getRandom(-720, 720),
          scale: 0,
          opacity: 0,
        })
      })

      var stagger_opts_to = {
        duration: 2,
        x: 0,
        y: 0,
        opacity: 1,
        scale: 1,
        rotation: 0,
        ease: 'power4.inOut',
        stagger: 0.0125,
      }

      this.localTl.to(paths, stagger_opts_to)

      function getRandom(min, max) {
        return Math.random() * (max - min) + min
      }
    },
    mouseover: function () {
      if (this.localTl != null) {
        this.localTl.timeScale(0.15)
      }
    },
    mouseleave: function () {
      if (this.localTl != null) {
        this.localTl.timeScale(1)
      }
    },
  },
}
</script>

